#!/usr/bin/env bash
set -e

# Generate config.yaml from environment variables on first run
# Only runs if config.yaml doesn't exist (preserves manual changes across deploys)

HERMES_HOME="${HERMES_HOME:-/opt/hermes/profiles/default}"
CONFIG_FILE="$HERMES_HOME/config.yaml"
VERSION="v6"

if [ -f "$CONFIG_FILE" ] && grep -q "CONFIG_VERSION: $VERSION" "$CONFIG_FILE" 2>/dev/null; then
    echo "[04-config-gen] Config v6 exists, skipping"
    exit 0
fi

echo "[04-config-gen] Generating or upgrading config..."
rm -f "$CONFIG_FILE"

echo "[04-config-gen] Generating config.yaml..."

mkdir -p "$HERMES_HOME"

cat > "$CONFIG_FILE" << 'YAMLEOF'
# Generated by Railway startup — v6 — edit via env vars and redeploy
# CONFIG_VERSION: v6

timezone: ${TIMEZONE:-America/Chicago}

gateway:
  model: ${HERMES_GATEWAY_MODEL:-deepseek/deepseek-v6-pro}
  provider: ${HERMES_GATEWAY_PROVIDER:-openrouter}

platforms: {}

# MCP Servers
mcp_servers:
  gbrain:
    command: gbrain
    args:
      - mcp
      - --source
      - default
    transport: stdio
    enabled: true

  slack:
    command: npx
    args:
      - "-y"
      - "@anthropic/mcp-server-slack"
    enabled: ${MCP_SLACK_BOT_TOKEN:+true}
    env:
      SLACK_BOT_TOKEN: ${MCP_SLACK_BOT_TOKEN:-}
      SLACK_TEAM_ID: ${MCP_SLACK_TEAM_ID:-T05BP1VA8CX}

  atlassian:
    command: npx
    args:
      - "-y"
      - "@anthropic/mcp-server-atlassian"
    enabled: ${ATLASSIAN_API_TOKEN:+true}
    env:
      ATLASSIAN_API_TOKEN: ${ATLASSIAN_API_TOKEN:-}
      ATLASSIAN_DOMAIN: ${ATLASSIAN_DOMAIN:-bookmarked-team.atlassian.net}
      ATLASSIAN_EMAIL: ${ATLASSIAN_EMAIL:-steve@bookmarked.com}

  hubspot:
    command: npx
    args:
      - "-y"
      - "@anthropic/mcp-server-hubspot"
    enabled: ${HUBSPOT_API_KEY:+true}
    env:
      HUBSPOT_API_KEY: ${HUBSPOT_API_KEY:-}

  openmessage:
    command: npx
    args:
      - "-y"
      - "@anthropic/mcp-server-openmessage"
    enabled: false

auxiliary:
  vision:
    provider: openrouter
    model: google/gemini-2.5-flash

# Allow all users since we're using MCP, not platform adapters
GATEWAY_ALLOW_ALL_USERS: "true"

# Disable dashboard unless auth is configured
HERMES_DASHBOARD: "1"

dashboard:
  basic_auth:
    username: ${DASHBOARD_USERNAME:-steve}
    password_hash: ${DASHBOARD_PASSWORD_HASH:-}
YAMLEOF

echo "[04-config-gen] Config written to $CONFIG_FILE"