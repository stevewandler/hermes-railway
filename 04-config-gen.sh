#!/command/with-contenv bash
# IMPORTANT: with-contenv (not bare bash) so this script inherits the s6
# container environment — i.e. the Railway service env vars (GBRAIN_MCP_URL,
# GBRAIN_MCP_TOKEN, etc.). With plain `#!/usr/bin/env bash` these are EMPTY at
# cont-init time even though they exist in /run/s6/container_environment/,
# which silently disabled the gbrain MCP on every deploy. See v19 notes.
set -e

HERMES_HOME="${HERMES_HOME:-/opt/data}"
PROFILE_CONFIG="/opt/hermes/profiles/default/config.yaml"
VOLUME_CONFIG="$HERMES_HOME/config.yaml"
VERSION="v19"

if [ -f "$PROFILE_CONFIG" ] && grep -q "CONFIG_VERSION: $VERSION" "$PROFILE_CONFIG" 2>/dev/null; then
    echo "[04-config-gen] Config $VERSION exists, skipping"
    exit 0
fi

echo "[04-config-gen] Generating config.yaml ($VERSION)..."

mkdir -p "$(dirname "$PROFILE_CONFIG")"
mkdir -p "$(dirname "$VOLUME_CONFIG")"

# gbrain remote MCP — values come from Railway env vars. Hermes does NOT expand
# ${...} inside config.yaml, so we expand them here at generation time.
# Belt-and-suspenders: if with-contenv somehow didn't import them, fall back to
# reading the s6 container_environment dir directly.
S6ENV="/run/s6/container_environment"
GBRAIN_URL="${GBRAIN_MCP_URL:-}"
GBRAIN_TOKEN="${GBRAIN_MCP_TOKEN:-}"
[ -z "$GBRAIN_URL" ]   && [ -r "$S6ENV/GBRAIN_MCP_URL" ]   && GBRAIN_URL="$(cat "$S6ENV/GBRAIN_MCP_URL")"
[ -z "$GBRAIN_TOKEN" ] && [ -r "$S6ENV/GBRAIN_MCP_TOKEN" ] && GBRAIN_TOKEN="$(cat "$S6ENV/GBRAIN_MCP_TOKEN")"

if [ -n "$GBRAIN_URL" ] && [ -n "$GBRAIN_TOKEN" ]; then
    GBRAIN_ENABLED="true"
    echo "[04-config-gen] gbrain MCP: enabled (url=$GBRAIN_URL)"
else
    GBRAIN_ENABLED="false"
    echo "[04-config-gen] WARN: GBRAIN_MCP_URL/TOKEN not found (env or $S6ENV) — gbrain MCP disabled"
fi

TEMP_CONFIG=$(mktemp)
cat > "$TEMP_CONFIG" << YAMLEOF
# Generated by Railway startup — v19
# CONFIG_VERSION: v19

timezone: America/Chicago

gateway:
  model: anthropic/claude-opus-4-6
  provider: openrouter

platforms: {}

mcp_servers:
  # Remote gbrain knowledge brain, reached over HTTP with a static bearer token.
  # URL + token expanded from Railway env vars at config-gen time.
  gbrain:
    enabled: ${GBRAIN_ENABLED}
    url: "${GBRAIN_URL}"
    headers:
      Authorization: "Bearer ${GBRAIN_TOKEN}"
    connect_timeout: 60
    read_timeout: 120

auxiliary:
  vision:
    provider: openrouter
    model: google/gemini-2.5-flash
YAMLEOF

cp "$TEMP_CONFIG" "$PROFILE_CONFIG"
cp "$TEMP_CONFIG" "$VOLUME_CONFIG"
rm -f "$TEMP_CONFIG"

echo "[04-config-gen] Config written (gbrain enabled=$GBRAIN_ENABLED)"

# Auth: basic (username/password) provider via HERMES_DASHBOARD_BASIC_AUTH_* env vars.
echo "[04-config-gen] Dashboard auth: basic provider (env-configured)"
